The following money formats are currently supported.
<ul>
    <li>$100.00</li>
    <li>$1,000.00</li>
    <li>100.00</li>
    <li>100</li>
</ul>
Money values may contain the dollar symbol ( $ ), commas ( , ) and decimal points ( . ).  Money values may not be negative.
Any money values with a precision greater than 2 will be rounded half up to the nearest full cent.
