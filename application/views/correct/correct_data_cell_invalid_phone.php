<div>
    Phone numbers should be international or domestic formatted values for the United States of America.
    Examples of accepted formats are:
    <ul>
        <li>##########</li>
        <li>###-###-####</li>
        <li>1###-###-####</li>
        <li>(###) ###-####</li>
        <li>1(###) ###-####</li>
        <li>+1(###) ###-####</li>
    </ul>
